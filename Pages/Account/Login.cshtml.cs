using IDFK.Data;
using Microsoft.AspNetCore.Authentication; // ???? ???? SignInAsync
using Microsoft.AspNetCore.Authentication.Cookies; // ???? ???? Cookie Scheme
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using System.Security.Claims; // ???? ???? Claims
using IDFK.Models; // ????? ????? ???? ?? ????? ?????? (User)
using Microsoft.AspNetCore.Identity; // ????? ????? ???? ?? PasswordHasher ?-PasswordVerificationResult



namespace IDFK.Pages.Account
{
    public class LoginModel : PageModel
    {
        private readonly ApplicationDbContext _context;
        public LoginModel(ApplicationDbContext context) => _context = context;

        [BindProperty]
        public string Email { get; set; }
        [BindProperty]
        public string Password { get; set; }
        public string Message { get; set; }

        public async Task<IActionResult> OnPostAsync()
        {
            // 1. ???? ?? ?????? ?? ?????? ??? ??????? ????
            var user = await _context.Users.FirstOrDefaultAsync(u => u.Email == Email);

            if (user != null)
            {
                // 2. ????? ?-Hasher ??????
                var passwordHasher = new PasswordHasher<User>();

                // 3. ????? ?????? ?????? ??? ?????? ??????? ???? ???????
                var result = passwordHasher.VerifyHashedPassword(user, user.Password, Password);

                if (result == PasswordVerificationResult.Success)
                {
                    // ?? ?????? ????? - ?????? ?? ?-Claims ????????
                    var claims = new List<Claim>
                    {
                        new Claim(ClaimTypes.Name, user.Username),
                        new Claim(ClaimTypes.Email, user.Email),
                        // ????? ????? ????? ??????:
                        new Claim(ClaimTypes.NameIdentifier, user.Id.ToString()),
                        // ????? ????? (Role) - ????? ??? ?????? ???????? (Admin)
                        new Claim(ClaimTypes.Role, user.Username == "Admin" ? "Admin" : "User"),
                        new Claim("ProfilePicture", user.ProfilePicturePath ?? "/images/default-avatar.png")
                    };

                    var claimsIdentity = new ClaimsIdentity(claims, CookieAuthenticationDefaults.AuthenticationScheme);
                    await HttpContext.SignInAsync(CookieAuthenticationDefaults.AuthenticationScheme, new ClaimsPrincipal(claimsIdentity));

                    return RedirectToPage("/Index");
                }
            }

            // ?? ?????? ?? ???? ?? ??????? ?? ?????
            Message = "Invalid email or password";
            return Page();
        }

    }
}
